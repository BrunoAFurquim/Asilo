package model;

import java.util.Date;

/**
 *
 * @author saraa
 */
public class DoacaoMonetaria {
    private float valor;
    private Date data;

    public DoacaoMonetaria(float valor, Date data) {
        this.valor = valor;
        this.data = data;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }
    
    
}
