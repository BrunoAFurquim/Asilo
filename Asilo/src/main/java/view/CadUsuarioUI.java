    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed

        String nome = this.txtNome.getText();
        String email = this.txtEmail.getText();
        String tel = this.txtTel.getText();
        String end = this.txtEnd.getText();
        String senha = new String(this.txtPass.getPassword());

        if (senha.isBlank() || senha.length() < 4) {
            this.labelErro.setText("*A senha deve ter mais de 4 caracteres");
            this.labelErro.setVisible(true);
        } else if (controll.getUsuarioPorEmail(email) != null) {
            this.labelErro.setText("*Email j√° cadastrado");
            this.labelErro.setVisible(true);
        } else {

            Usuario u = controll.cadastrarUsuario(nome, email, senha, end, tel);

            if (u != null) {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Falha ao cadastrar");
            }
        }

    }//GEN-LAST:event_btnActionPerformed

