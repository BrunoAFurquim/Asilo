    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nova = new String (this.jPasswordField1.getPassword());
        String confirm = new String (this.jPasswordField2.getPassword());
        
        if(nova.equals(confirm)){
            if(nova.length()>4){
                if(this.usuario != null){
                    this.usuario = this.usuarioControll.atualizarSenhaUsuario(this.usuario.getId(), nova);
                    if(usuario != null){
                        JOptionPane.showMessageDialog(null, "Senha alterado com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Falha na alteração.");
                    }
                    this.dispose();
                } else if(this.adm != null){
                    this.adm = this.admControll.atualizarSenhaAdm(nova);
                    if(this.adm!= null){
                        JOptionPane.showMessageDialog(null, "Senha alterado com sucesso!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Falha na alteração.");
                    }
                    this.dispose();
                }
            } else {
                this.labelErro.setText("*A senha deve ter mais de 4 caracteres");
                this.labelErro.setVisible(true);
            }
        } else{
                this.labelErro.setText("*As senhas inseridas são diferentes");
                this.labelErro.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

